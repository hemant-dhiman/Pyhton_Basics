#special operator have two type
#
# 1.  Identity Operator
#   1. is
#   2. is not
# 2. Membership Operator
#   1.in
#   2.not in
print('E.g. for "is" operator')
x='hello'
y='hello'
print("x='hello' and y='hello'")
print('x is identical to y?',x is y)

print('\nE.g. for "is not" operator')
x='hello'
y='hello'
print("x='hello' and y='hello'")
print('x is not identical to y?',x is not y)

print('\nE.g. for "in" operator')
x='hello world!!'
print("x='hello world!!'")
print("'hello' in x?",'hello' in x)
print("'Hello' in x?",'Hello' in x)

print('\nE.g. for "not in" operator')
x='hello world!!'
print("x='hello world!!'")
print("'hello' not in x?",'hello' not in x)
print("'Hello' not in x?",'Hello' not in x)