# 1.    type() -> it return the type of an object!

print('E.g. of type() function!')
w={1:'a',2:5}
x=1
y=1.6
z='hello world!!'

print('w={1:"a",2:5}\nx=1\ny=1.6\nz="hello world!!"')
print('w from ',type(w))
print('x from ',type(x))
print('y from ',type(y))
print('z from ',type(z))

# 2.    int() -> it return integer value
print('\nE.g. of int() function!')
x=11.4
y=66
c='123'
print("x=11.4\ny=66\nc='123'")
print('int(x) returns',int(x))
print('int(y) returns',int(y))
print('int(c) returns',int(c))

# 3.    float() -> it return float value
print('\nE.g. of float() function!')
print("x=11.4\ny=66\nc='123'")
print('float(x) returns',float(x))
print('float(y) returns',float(y))
print('float(c) returns',float(c))

# 4.    str() -> it return string value
print('\nE.g. of str() function!')
d='x'
print("x=11.4\ny=66\nd='x'")
print('str(x) returns',str(x))
